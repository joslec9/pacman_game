function moveGhost1() {
    var random = Math.floor(Math.random() * 4) + 37;
    console.log("ghost key is:", random);
    if (random === 37) {
        if (world[ghost1.y][ghost1.x-1] != 2) {
            $('#ghost1').attr('class', 'ghost1-L');
            ghost1.x--;
        }
    } else if (random === 38) {
        if (world[ghost1.y-1][ghost1.x] != 2) {
            $('#ghost1').attr('class', 'ghost1-U');
            ghost1.y--;
        }
    } else if (random === 39) {
        if (world[ghost1.y][ghost1.x+1] != 2) {
            $('#ghost1').attr('class', 'ghost1-R');
            ghost1.x++;
        }
    } else if (random === 40) {
        if (world[ghost1.y+1][ghost1.x] != 2) {
            $('#ghost1').attr('class', 'ghost1-D');
            ghost1.y++;
        }
    }
    
    if (ghost1.y === pacman.y && ghost1.x === pacman.x){
        lives -= 1;
        pacman = {
            x: 1,
            y: 1
        };
        $("#pacman").css('transform', 'rotate(0deg)');
        displayPacman();
        displayScore();
    }
    if(lives === 0){
        active = false;
        $('#gameover').show();
    }
    if(active){
    displayGhost1();
    displayWorld();
    }
}

